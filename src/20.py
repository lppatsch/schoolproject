def add_numbers(x, y):
    """
    Add two numbers and return the result.
    
    Parameters:
        x (int): The first number to be added.
        y (int): The second number to be added.
        
    Returns:
        int: The sum of x and y.
    """
    return x + y

result = add_numbers(5, 3)
print(result)  # Output: 8
